package com.example.myapplication12121;

public class Task {
    String name;
    String date;
    String note;
    boolean box;
    int editImage;
    int delImage;

    Task(String name, String date, String note, boolean box, int editImage, int delImage){
        this.name = name;
        this.date = date;
        this.note = note;
        this.box = box;
        this.editImage = editImage;
        this.delImage = delImage;
    }
}
